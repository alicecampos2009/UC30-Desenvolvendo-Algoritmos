def calculoSoma(valor1, valor2):
    soma = (valor1 + valor2)
    return soma

resultado = calculoSoma (5.0, 7.0)
print(f"O resultado da soma é de: {resultado}")

def calculoMulti(valor1, valor2):
    multi = (valor1 * valor2)
    return multi

resultado = calculoMulti (2, 4)
print(f"O resultado da multiplicação é de: {resultado}")